# Faça um programa que leia um número intero qualquer e mostre na tela sua tabuada
# ax1 ax2 ax3 ax4 fazer bonito.
t = int(input('Digite um número: '))
print('{0}x1={0}\n{0}x2={1}\n{0}x3={2}\n{0}x4={3}\n{0}x5={4}\n{0}x6={5}\n{0}x7={6}\n{0}x8={7}\n{0}x9={8}\n{0}x10={9}'.format(t, t*2, t*3, t*4, t*5, t*6, t*7, t*8, t*9, t*10))
